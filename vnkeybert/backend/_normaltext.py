  def convert_unicode(self,txt):
      return re.sub(
          r'à|á|ả|ã|ạ|ầ|ấ|ẩ|ẫ|ậ|ằ|ắ|ẳ|ẵ|ặ|è|é|ẻ|ẽ|ẹ|ề|ế|ể|ễ|ệ|ì|í|ỉ|ĩ|ị|ò|ó|ỏ|õ|ọ|ồ|ố|ổ|ỗ|ộ|ờ|ớ|ở|ỡ|ợ|ù|ú|ủ|ũ|ụ|ừ|ứ|ử|ữ|ự|ỳ|ý|ỷ|ỹ|ỵ|À|Á|Ả|Ã|Ạ|Ầ|Ấ|Ẩ|Ẫ|Ậ|Ằ|Ắ|Ẳ|Ẵ|Ặ|È|É|Ẻ|Ẽ|Ẹ|Ề|Ế|Ể|Ễ|Ệ|Ì|Í|Ỉ|Ĩ|Ị|Ò|Ó|Ỏ|Õ|Ọ|Ồ|Ố|Ổ|Ỗ|Ộ|Ờ|Ớ|Ở|Ỡ|Ợ|Ù|Ú|Ủ|Ũ|Ụ|Ừ|Ứ|Ử|Ữ|Ự|Ỳ|Ý|Ỷ|Ỹ|Ỵ',
          lambda x: self.dicchar[x.group()], txt)

  # remove URL
  def remove_url(self,document):
    document = re.sub(r'[A-Za-z0-9]+://[A-Za-z0-9%-_]+(/[A-Za-z0-9%-_])*(#|\\?)[A-Za-z0-9%-_&=]*','',document)
    return document

  # remove html
  def remove_html(self,document):
    document = re.sub(r'<[^>]*>','',document)
    return document

  # remove punctuation
  def remove_punct(self, document):
    document = re.sub(r'[^\w\s_]',' ',document)
    document = re.sub(r'\s{2,}',' ', document)
    return document

  def normal_text(self, documents:List[str])-> List[str]:
    docs_normal = []
    for doc in documents:
      doc = self.convert_unicode(doc)
      doc = self.remove_url(doc)
      doc = self.remove_html(doc)
      doc = self.remove_punct(doc)
      docs_normal.append(doc)
    return docs_normal


